    ONE_SECOND = 1   # 一秒钟
    ONE_MINUTE = 1   # 一分钟
    ONE_HOUR = 1   # 一小时
    ONE_DAY = 1   # 一天
    ONE_MONTH = 1    # 一个月
    ONE_YEAR = 1   # 一年
